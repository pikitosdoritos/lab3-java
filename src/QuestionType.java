import java.util.List;

// Інтерфейс QuestionType — це реалізація патерну Flyweight
// Він описує СПІЛЬНУ поведінку для всіх типів питань
// Наприклад: питання з однією відповіддю, з кількома і т.д.
// Об'єкти цього інтерфейсу будуть перевикористовуватись через фабрику,
// тобто не створюватись кожен раз заново
public interface QuestionType {

    // Метод повертає назву типу питання
    // Наприклад: "Одна правильна відповідь" або "Кілька відповідей"
    // Використовується для виводу або логіки
    String getTypeName();

    // Метод виводить інструкцію студенту
    // Тобто пояснює, як відповідати на це питання
    // Наприклад: "Введіть номер відповіді" або "Введіть декілька через кому"
    void printInstruction();

    // Основний метод — перевірка відповіді
    // question — саме питання з варіантами (це зовнішній стан у Flyweight)
    // studentAnswer — те, що ввів студент
    // Метод повертає true якщо відповідь правильна, і false якщо ні
    boolean checkAnswer(Question question, String studentAnswer);
}